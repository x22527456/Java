public class Repetition4{
	public static void main(String args[]){

		//print ***** 3 times

		for(int row = 1; row <=3; row = row + 1){//initialisation; termination; progression
			for(int i = 1; i <=5; i = i + 1){
					System.out.print("*");//action
				}
				System.out.println();
			}
}
}
